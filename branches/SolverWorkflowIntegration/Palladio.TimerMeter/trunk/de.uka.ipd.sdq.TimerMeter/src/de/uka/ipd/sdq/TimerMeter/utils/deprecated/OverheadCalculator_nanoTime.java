	@SuppressWarnings("unused")
	private void disableThreadTimer(ThreadMXBean pBean){
		if(pBean.isThreadCpuTimeSupported()){
			o.println("Thread CPU time supported");
			if(pBean.isThreadCpuTimeEnabled()){
				o.println("Thread CPU time was enabled");
				pBean.setThreadCpuTimeEnabled(false);
				o.println("Thread CPU time SHOULD now be DISabled");
			}else{
				o.println("Thread CPU time was already DISabled");
			}
		}else{
			o.println("Thread CPU time NOT supported");
			if(pBean.isCurrentThreadCpuTimeSupported()){
				o.println("But *current* thread CPU time supported!");
			}else{
				o.println("Even *current* thread CPU time is NOT supported!");
			}
		}
	
	}

	private void enableThreadTimer(ThreadMXBean pBean){
		if(pBean.isThreadCpuTimeSupported()){
			o.println("Thread CPU time supported");
			if(pBean.isThreadCpuTimeEnabled()){
				o.println("Thread CPU time was enabled");
			}else{
				pBean.setThreadCpuTimeEnabled(true);
				o.println("Thread CPU time SHOULD now be enabled");
			}
		}else{
			o.println("Thread CPU time NOT supported");
			if(pBean.isCurrentThreadCpuTimeSupported()){
				o.println("But *current* thread CPU time supported!");
			}else{
				o.println("Even *current* thread CPU time is NOT supported!");
			}
		}
	}

